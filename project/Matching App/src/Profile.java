    private void _heightKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event__heightKeyReleased
        // TODO add your handling code here:
        try {
            int height = Integer.parseInt(_height.getText());
            if (!(height >= 0 && height <= 300)) {
                JOptionPane.showMessageDialog(this, "Height must be between 0 and 300!!!", "Invalid Height", HEIGHT);
                _height.setText("");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Height must be digits!!!");
            _height.setText("");
        }
    }//GEN-LAST:event__heightKeyReleased

